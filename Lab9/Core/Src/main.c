void step(int32_t dx, int32_t dy, int8_t mys)
{
	uint8_t buff[4];
	buff[0] = mys; // stiskni leve tlacitko 0x01
	buff[1] = (int8_t)(dx);
	buff[2] = (int8_t)(dy);
	buff[3] = 0; // bez scrollu

	USBD_HID_SendReport(&hUsbDeviceFS, buff, sizeof(buff));
	HAL_Delay(USBD_HID_GetPollingInterval(&hUsbDeviceFS));
}

