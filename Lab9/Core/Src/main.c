void circle(uint8_t r)
{
	  float x = r;
	  float y = 0;

	  int32_t sx;
	  int32_t sy;
	  int32_t dx;
	  int32_t dy;

	for(uint16_t phi= 0; phi<=360; phi = phi + 5)
	{
	 	  sx = x;
		  sy = y;

		  x = r*cos(phi*PI/180);
		  y = r*sin(phi*PI/180);

		  dx = x-sx;
		  dy = y-sy;

		  step(dx,dy,0x01);
	}

	x = 0;
	y = 0;
	step(x,y,0x00);
}

